@Controller
@RequestMapping("/routeTemplate") 
public class RouteTemplateController extends BaseController{
	
	private final static Logger log= Logger.getLogger(RouteTemplateController.class);
	
	// Servrice start
	@Autowired(required=false) //自动注入，不需要生成set方法了，required=false表示没有实现类，也不会报错。
	private RouteTemplateService<RouteTemplate> routeTemplateService; 
	
	
	
	
	
	/**
	 * 
	 * @param url
	 * @param classifyId
	 * @return
	 * @throws Exception 
	 */
	@RequestMapping("/list") 
	public ModelAndView  list(RouteTemplatePage page,HttpServletRequest request) throws Exception{
		Map<String,Object>  context = getRootMap();
		List<RouteTemplate> dataList = routeTemplateService.queryByList(page);
		//设置页面数据
		context.put("dataList", dataList);
		return forword("server/sys/routeTemplate",context); 
	}
	
	
	/**
	 * @param url
	 * @param classifyId
	 * @return
	 * @throws Exception 
	 */
	@RequestMapping("/dataList") 
	public void  datalist(RouteTemplatePage page,HttpServletResponse response) throws Exception{
		List<RouteTemplate> dataList = routeTemplateService.queryByList(page);
		//设置页面数据
		Map<String,Object> jsonMap = new HashMap<String,Object>();
		jsonMap.put("total",page.getPager().getRowCount());
		jsonMap.put("rows", dataList);
		HtmlUtil.writerJSON(response, jsonMap);
	}
	
	/**
	 * 添加或修改数据
	 * @param url
	 * @param classifyId
	 * @return
	 * @throws Exception 
	 */
	@RequestMapping("/save")
	public void save(RouteTemplate entity,Integer[] typeIds,HttpServletResponse response) throws Exception{
		Map<String,Object>  context = new HashMap<String,Object>();
		if(entity.getId()==null||StringUtils.isBlank(entity.getId().toString())){
			routeTemplateService.add(entity);
		}else{
			RouteTemplate rt = routeTemplateService.queryById(entity.getId());
			if(rt == null)
				routeTemplateService.add(entity);
			else
				routeTemplateService.update(entity);
		}
		sendSuccessMessage(response, "保存成功~");
	}
	
	
	@RequestMapping("/getId")
	public void getId(String id,HttpServletResponse response) throws Exception{
		Map<String,Object>  context = new HashMap();
		RouteTemplate entity  = routeTemplateService.queryById(id);
		if(entity  == null){
			sendFailureMessage(response, "没有找到对应的记录!");
			return;
		}
		context.put(SUCCESS, true);
		context.put("data", entity);
		HtmlUtil.writerJSON(response, context);
	}
	
	
	
	@RequestMapping("/delete")
	public void delete(String[] id,HttpServletResponse response) throws Exception{
		routeTemplateService.delete(id);
		sendSuccessMessage(response, "删除成功");
	}

}
